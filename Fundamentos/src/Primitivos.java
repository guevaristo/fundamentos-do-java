
public class Primitivos {

	public static void main(String[] args) {
				
		// Tipo booleano (veradeiro ou falso)
		boolean completo = true;
	
		//tipo char (1 caracter unicode 'aspas simpes')
		char sexo = 'F';
		
		//tipo byte (numeros inteiros)
		byte idade = 18;
		
		//tipo short (Números inteiros)
		short numero1 = 16780;
		
		//tipo padrão para números inteiros
		int numero2 = 1968;
		
		//tipo long (números inteiros "L" no final)
		long numero3 = 214748364l;		
		
		//tipo float (números não inteiros, digitar "F" no final)
		float nota1 = 5.6f;
		
		//tipo double (padrão para números não inteiros)
		double nota2 = 7.8;  
		
		//String (Cadeia de caracteres) "aspas"
		String nome ="Gustavo";
				

		System.out.println(completo);
		System.out.println(sexo);
		System.out.println(idade);
		System.out.println(numero1);
		System.out.println(numero2);
		System.out.println(numero3);
		System.out.println(nota1);
		System.out.println(nota2);
		System.out.println(nome);
	}
}
